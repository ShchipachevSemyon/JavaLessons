package kyrs.Example;

public class ForAndWhile {
    public static void main(String[] args) {
        for (int i =2000;i<=2100;i++){
            System.out.println("Номер билета "+i);
        }
        for (int i = 2200;i<=2350;i++){
            System.out.println("Номер билета " +i);
        }
        int i =2000;
        while (i<=2100){
            System.out.println(i);
            i++;
        }
        i = 2200;
        while (i<=2350){
            System.out.println(i);
            i++;
        }
    }
}
